// 特定盤面を含む解を抽出
function filterSolutions(code) {
	const boardCode = getCodeFromBoard();
	if(boardCode === ALL_BLANK) { return ALL_SOLUTION_LIST; }
	let solutions = [];
	ALL_SOLUTION_LIST.forEach(s => {
		// 0b0001 がオン - 通常解が候補
		// 0b0010 がオン - 左右反転解が候補
		// 0b0100 がオン - 上下反転解が候補
		// 0b1000 がオン - 180度回転解が候補
		let isValidAnswer = 0b1111;
		for(i=0; i<60; i++){
			if(boardCode[i] === BLANK_LETTER){ continue;}
			const X = i % 10;
			const Y = Math.floor(i/10);
			
			if(boardCode[i] !== s[(  Y)*10+(  X)]){ isValidAnswer = isValidAnswer & 0b1110; }
			if(boardCode[i] !== s[(  Y)*10+(9-X)]){ isValidAnswer = isValidAnswer & 0b1101; }
			if(boardCode[i] !== s[(5-Y)*10+(  X)]){ isValidAnswer = isValidAnswer & 0b1011; }
			if(boardCode[i] !== s[(5-Y)*10+(9-X)]){ isValidAnswer = isValidAnswer & 0b0111; }
			if(!isValidAnswer){ break; }
		}
		// 反転・回転で成立する解が複数ある場合、通常→左右反転→上下反転→180度回転の順で処理
		if(isValidAnswer & 0b0001) { return solutions.push(s)}
		if(isValidAnswer & 0b0010) { return solutions.push(rotateCode(s,1));}
		if(isValidAnswer & 0b0100) { return solutions.push(rotateCode(s,2));}
		if(isValidAnswer & 0b1000) { return solutions.push(rotateCode(s,3));}
	});
	return solutions.sort();
}

