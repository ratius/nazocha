// 改ページ処理
const changePage = (page) => {
	const lastPageNumber = Math.max(Math.ceil(currentSolutions.length / 20)-1, 0);
	if(page > 0) { // 先のページへ
		currentPage = Math.min(currentPage + page, lastPageNumber);
	} else if (page < 0){ // 前のページへ
		currentPage = Math.max(currentPage + page, 0);
	} else {
		currentPage = 0;
	}
	// ページ送りボタンの変更
	document.getElementById('PageChangeFirst').disabled = (currentPage === 0);
	document.getElementById('PageChangePrev').disabled  = (currentPage === 0);
	document.getElementById('PageChangeNext').disabled  = (currentPage >= lastPageNumber);
	document.getElementById('PageChangeLast').disabled  = (currentPage >= lastPageNumber);

	//解がない場合の処理
	if(currentSolutions.length === 0){
		document.getElementById('currentPageInfo').innerHTML = "";
		document.getElementById('solutionNumber').innerHTML = "(全 0 解)";
		document.getElementById('PuzzleSolutions').innerHTML = "見つかりませんでした。";
		return;
	}

	document.getElementById('currentPageInfo').innerHTML = `${currentPage+1} / ${lastPageNumber+1} ページ`;
	document.getElementById('solutionNumber').innerHTML = `(全 ${currentSolutions.length} 解中 ${currentPage*20 + 1} - ${Math.min(currentPage*20 + 20, currentSolutions.length)} を表示中)`
	
	const solutionsTemp = currentSolutions.slice(currentPage*SOLUTIONS_PER_PAGE, (currentPage+1)*SOLUTIONS_PER_PAGE);
	const output = solutionsTemp.map((solution, index) => {
		let temp = `<div class="solution"><div class="solution-number">#${currentPage * 20 + index + 1}</div><div class="solution-cells-wrapper">`;
		for(i=0; i<60; i++){
			let cellClass = `solution-cell solution-cell-${solution[i]}`;
			if(i%10 === 9 || solution[i] !== solution[i+1]){ cellClass += " cell-rb";}
			if(i >= 50 || solution[i] !== solution[i+10]){ cellClass += " cell-bb";}
			temp += `<div class="${cellClass}">${solution[i]}</div>`
		}
		return temp + `</div></div>`;
	}).join('');
	document.getElementById('PuzzleSolutions').innerHTML = output;
	document.getElementById('PuzzleSolutions').scrollTop = 0;
};

