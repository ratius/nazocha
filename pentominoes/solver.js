// コードを反転・回転
// (rot%4)によって処理が変わる - 0:そのまま 1:左右反転 2:上下反転 3:180度回転
const rotateCode = (code, rot = 0) => {
	if(code.length !== 60){ throw new Error("コードが60文字ではありません");}
	if(rot & 2){ //縦に反転させる
		let newCode = "";
		for(i=0; i<60; i++){
			newCode += code[(5 - Math.floor(i/10)) * 10 + (i % 10)];
		}
		code = newCode;
	}
	if(rot & 1){ //横に反転させる
		let newCode = "";
		for(i=0; i<60; i++){
			newCode += code[Math.floor(i/10) * 10 + 9 - (i % 10)];
		}
		code = newCode;
	}
	return code;
};

